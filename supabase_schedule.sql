-- =====================================================================
--  מה הקובץ הזה עושה:
--  1. יוצר טבלה schedule_entries — שורות בלו"ז השבועי של המאמן
--     (קבוצה, יום בשבוע, שעה, ותוכנית אימון מצורפת — אופציונלי).
--  2. אבטחה (RLS): הלו"ז פרטי — כל מאמן רואה ומנהל רק את שלו.
--
--  הוראות: העתק את כל הקובץ והרץ ב-SQL Editor של Supabase.
--  בטוח להריץ אותו יותר מפעם אחת.
-- =====================================================================


-- ---------------------------------------------------------------------
-- 1) יצירת טבלת הלו"ז
-- ---------------------------------------------------------------------
create table if not exists public.schedule_entries (
  id          uuid primary key default gen_random_uuid(),

  created_by  uuid not null default auth.uid()
              references public.profiles(id) on delete cascade,

  team        text not null,        -- שם הקבוצה
  day_of_week smallint not null,    -- 0=ראשון, 1=שני, ... 6=שבת
  time        text,                 -- שעה (טקסט חופשי, למשל "17:00")

  -- תוכנית אימון מצורפת (אם התוכנית נמחקת — הקישור מתאפס, השורה נשארת)
  plan_id     uuid references public.training_plans(id) on delete set null,

  note        text,
  created_at  timestamptz default now()
);


-- ---------------------------------------------------------------------
-- 2) הפעלת אבטחה (RLS)
-- ---------------------------------------------------------------------
alter table public.schedule_entries enable row level security;


-- ---------------------------------------------------------------------
-- 3) מדיניות אבטחה — לו"ז פרטי לחלוטין
-- ---------------------------------------------------------------------
drop policy if exists "schedule_all_own" on public.schedule_entries;
create policy "schedule_all_own"
  on public.schedule_entries for all
  to authenticated
  using (created_by = auth.uid())
  with check (created_by = auth.uid());


-- ---------------------------------------------------------------------
-- 4) רענון ה-cache של Supabase (כדי שהטבלה תזוהה מיד)
-- ---------------------------------------------------------------------
notify pgrst, 'reload schema';



-- =====================================================================
--  פנקס המאמן — שלב 2: מאתר מאמנים (אבטחה + אימייל)
-- =====================================================================
--  מה הקובץ הזה עושה:
--  1. מוסיף עמודת email לטבלת profiles.
--  2. מעדכן את הטריגר כך שימלא את האימייל אוטומטית בכל הרשמה חדשה.
--  3. ממלא את האימייל למאמנים שכבר נרשמו (backfill).
--  4. משנה את האבטחה: כל מאמן מחובר יכול לקרוא את כל הפרופילים
--     (לצורך החיפוש), אבל לערוך רק את הפרופיל של עצמו.
--
--  הוראות: העתק את כל הקובץ והרץ אותו ב-SQL Editor של Supabase.
--  בטוח להריץ אותו יותר מפעם אחת.
-- =====================================================================


-- ---------------------------------------------------------------------
-- 1) הוספת עמודת אימייל
-- ---------------------------------------------------------------------
-- "if not exists" => בטוח להריץ שוב, לא ייתן שגיאה אם העמודה כבר קיימת.

alter table public.profiles
  add column if not exists email text;


-- ---------------------------------------------------------------------
-- 2) עדכון הטריגר — שימלא גם את האימייל
-- ---------------------------------------------------------------------
-- new.email הוא האימייל שאיתו המאמן נרשם זה עתה.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email)
  values (new.id, new.email)
  on conflict (id) do nothing;
  return new;
end;
$$;


-- ---------------------------------------------------------------------
-- 3) מילוי רטרואקטיבי (backfill) למאמנים קיימים
-- ---------------------------------------------------------------------
-- מי שנרשם לפני השינוי הזה — נשלים לו את האימייל מתוך auth.users.

update public.profiles p
set email = u.email
from auth.users u
where u.id = p.id
  and p.email is null;


-- ---------------------------------------------------------------------
-- 4) שינוי האבטחה (RLS)
-- ---------------------------------------------------------------------
-- מסירים את המדיניות הישנה ("רואה רק את עצמו") ומחליפים במדיניות
-- שמאפשרת לכל מאמן *מחובר* לקרוא את כל הפרופילים.
-- מדיניות העריכה/הוספה לא משתנות — עדיין רק על הפרופיל של עצמך.

drop policy if exists "profiles_select_own" on public.profiles;
drop policy if exists "profiles_select_authenticated" on public.profiles;

-- כל מאמן מחובר רשאי לקרוא את כל הפרופילים (לצורך החיפוש)
create policy "profiles_select_authenticated"
  on public.profiles for select
  to authenticated
  using (true);



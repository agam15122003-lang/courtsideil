-- ============================================================
-- ניקוי ספריית התרגילים — משאיר רק את "לימוד הטעיית כתף"
-- ============================================================
-- מוחק את כל תרגילי הדמו שנוספו מ-supabase_seed_drills.sql (ומכל מקור אחר),
-- ומשאיר אך ורק תרגילים ששמם מכיל "הטעיית כתף".
--
-- הערה: הדירוגים, התגובות, המועדפים ופריטי-התוכניות של התרגילים שנמחקים
-- נמחקים איתם אוטומטית (on delete cascade) — אין צורך בשלבים נוספים.
--
-- הרצה: Supabase → SQL Editor → New query → הדבק → Run.
-- לפני ההרצה אפשר לבדוק מה יימחק עם השאילתה הזו:
--   select title from public.drills where title not ilike '%הטעיית כתף%';

delete from public.drills
where title not ilike '%הטעיית כתף%';

-- בדיקה אחרי ההרצה — אמור להחזיר רק את התרגיל שלך:
select id, title, category, created_at
from public.drills
order by created_at;
